# 自定义日志处理器：按日期创建文件，避免重命名冲突
class DailyFileHandler(logging.FileHandler):
    """
    按日期创建日志文件的处理器，避免重命名正在使用的文件
    文件名格式：mtws.log.2025-09-28
    """
    def __init__(self, filename, mode='a', encoding=None, delay=False, backup_count=7):
        self.base_filename = filename
        self.backup_count = backup_count
        self.current_date = None
        # 日志目录是运行时产物，不进 git；首次启动时必须自动创建。
        Path(self.base_filename).parent.mkdir(parents=True, exist_ok=True)
        # 初始化时使用当前日期的文件名
        current_filename = self._get_current_filename()
        super().__init__(current_filename, mode, encoding, delay)
    
    def _get_current_filename(self):
        """根据当前日期生成文件名"""
        today = datetime.now().strftime('%Y-%m-%d')
        base_path = Path(self.base_filename)
        base_path.parent.mkdir(parents=True, exist_ok=True)
        return str(base_path.parent / f"{base_path.stem}.{today}{base_path.suffix}")
    
    def emit(self, record):
        """写入日志记录，检查是否需要切换文件"""
        today = datetime.now().strftime('%Y-%m-%d')
        
        # 如果日期变化，切换到新文件
        if self.current_date != today:
            self.current_date = today
            new_filename = self._get_current_filename()
            
            # 关闭当前文件
            if self.stream:
                self.stream.close()
                self.stream = None
            
            # 切换到新文件
            self.baseFilename = new_filename
            
            # 清理旧文件
            self._cleanup_old_files()
        
        # 写入日志
        super().emit(record)
    
    def _cleanup_old_files(self):
        """清理超过保留天数的旧日志文件"""
        try:
            base_path = Path(self.base_filename)
            log_dir = base_path.parent
            pattern = f"{base_path.stem}.????-??-??{base_path.suffix}"
            
            # 找到所有匹配的日志文件
            log_files = list(log_dir.glob(pattern))
            
            # 按修改时间排序，保留最新的backup_count个文件
            if len(log_files) > self.backup_count:
                log_files.sort(key=lambda x: x.stat().st_mtime, reverse=True)
                for old_file in log_files[self.backup_count:]:
                    try:
                        old_file.unlink()
                    except OSError:
                        pass  # 忽略删除失败的文件
        except Exception:
            pass  # 忽略清理过程中的错误

